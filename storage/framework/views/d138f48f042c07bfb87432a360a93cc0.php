    <script>
        // Update selected count
        function updateSelectedCount() {
            const selectedCount = document.querySelectorAll('.permission-checkbox:checked').length;
            const totalPermissions = <?php echo e($permissions->count()); ?>;
            
            const countElement = document.getElementById('selectedCount');
            if (selectedCount === 0) {
                countElement.textContent = '<?php echo e(__('global.no_permissions_selected')); ?>';
                countElement.className = 'text-slate-500';
            } else {
                countElement.textContent = `<?php echo e(__('global.selected_count')); ?>: ${selectedCount} <?php echo e(__('global.of')); ?> ${totalPermissions} <?php echo e(__('global.permissions')); ?>`;
                countElement.className = 'text-primary font-medium';
            }
        }
        
        // Select all permissions
        function selectAllPermissions() {
            <?php if($role->id != 1): ?>
            document.querySelectorAll('.permission-checkbox:not(:disabled)').forEach(checkbox => {
                checkbox.checked = true;
            });
            updateSelectedCount();
            <?php else: ?>
            alert('<?php echo e(__('global.admin_permissions_cannot_be_changed')); ?>');
            <?php endif; ?>
        }
        
        // Deselect all permissions
        function deselectAllPermissions() {
            <?php if($role->id != 1): ?>
            document.querySelectorAll('.permission-checkbox:not(:disabled)').forEach(checkbox => {
                checkbox.checked = false;
            });
            updateSelectedCount();
            <?php else: ?>
            alert('<?php echo e(__('global.admin_permissions_cannot_be_changed')); ?>');
            <?php endif; ?>
        }
        
        // Toggle category selection
        function toggleCategory(category) {
            <?php if($role->id != 1): ?>
            const categoryCheckboxes = document.querySelectorAll(`.permission-category[data-category="${category}"] .permission-checkbox:not(:disabled)`);
            const allChecked = Array.from(categoryCheckboxes).every(cb => cb.checked);
            
            categoryCheckboxes.forEach(checkbox => {
                checkbox.checked = !allChecked;
            });
            
            updateSelectedCount();
            <?php else: ?>
            alert('<?php echo e(__('global.admin_permissions_cannot_be_changed')); ?>');
            <?php endif; ?>
        }
        
        // Initialize event listeners
        document.addEventListener('DOMContentLoaded', function() {
            // Add event listeners to all checkboxes
            document.querySelectorAll('.permission-checkbox:not(:disabled)').forEach(checkbox => {
                checkbox.addEventListener('change', updateSelectedCount);
            });
            
            // Initialize count
            updateSelectedCount();
        });
        
        // Form submission handler
        document.querySelector('form').addEventListener('submit', function(e) {
            const selectedCount = document.querySelectorAll('.permission-checkbox:checked').length;
            const roleName = document.querySelector('input[name="name"]').value.trim();
            
            if (!roleName) {
                e.preventDefault();
                alert('<?php echo e(__('global.please_enter_role_name')); ?>');
                return;
            }
            
            // Confirm if no permissions selected (except for admin)
            if (selectedCount === 0 && roleName.toLowerCase() !== 'admin' && <?php echo e($role->id); ?> !== 1) {
                if (!confirm('<?php echo e(__('global.no_permissions_warning_edit')); ?>')) {
                    e.preventDefault();
                }
            }
        });
